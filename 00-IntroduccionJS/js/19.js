// Operadores de igualdad

const numero1 = 20;
const numero2 = 20;

/*
  ==  (Comparador no estricto)
  === (Comparador estricto)
*/

// Comparación estricta (===), sin conversión de tipo de dato
if (numero1 === numero2) {
  console.log("Si, son iguales");
} else {
  console.log("No, no son iguales");
}
